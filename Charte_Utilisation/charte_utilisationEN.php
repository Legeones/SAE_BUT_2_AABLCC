    <h3>1 Scope</h3>
    <h5>Unless otherwise stated, this charter applies to all users of the institution's information system, regardless of their status, including trainees and occasional visitors.</h5>
    <h3>2 Access Settings Privacy</h3>
    <h5>Access to certain elements of the information system (such as e-mail, sessions on workstations, the network, certain applications or interactive services) is protected by connection parameters (identifiers, passwords).
