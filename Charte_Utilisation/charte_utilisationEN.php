        Law n° 78-17 of January 6, 1978 relating to data processing, files and freedoms, defines the conditions under which the processing of personal data can be carried out. It establishes for the benefit of the persons concerned by the processing of the rights that this present invites to respect, both with regard to users and third parties.</h5>
    <h3>4 Penalties</h3>
    <h5>Failure to comply with the rules and security measures of this charter is likely to engage the responsibility of the user and lead to warnings, limitations or suspensions of use of all or part of the IS, or even penalties. disciplinary, in proportion to the seriousness of the facts concerned.</h5>
