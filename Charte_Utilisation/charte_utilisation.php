<h1>Charte d’utilisation</h1>
<h3>Préambule</h3>
<h5>L’IFSI de Maubeuge, met en œuvre un système d’information et de communication nécessaire à son activité, comprenant notamment un réseau informatique et téléphonique.<br>
    Les apprenties infirmiers, dans le cadre de leur formation sont conduits à accéder aux moyens de communication mis à leur disposition et à les utiliser.<br>
    L’utilisation du SI (Systèmes d’information) doit être effectué exclusivement dans le cadre de leur formation, sauf exception prévue dans la présente charte.<br>
    Dans un but de transparence à l’égard des utilisateurs, de promotion d’une utilisation loyale, responsable sécurisée du système d’information, la présente charte pose les règles relatives à l’utilisation de ces ressources.</h5>
<h3> 1 Champ d’application
    Utilisateurs concernés </h3>
<h5>Sauf mention contraire, la présente charte s’applique à l’ensemble des utilisateurs du système d’information de l’établissement, quel que soit leur statut y compris les stagiaires, et les visiteurs occasionnels.</h5>
<h3>2 Confidentialité des paramètres d’accès</h3>
<h5>L’accès à certains éléments du système d’information (comme la messagerie électronique, les sessions sur les postes de travail, le réseau, certaines applications ou services interactifs) est protégé par des paramètres de connexion (identifiants, mots de passe).
    Ces paramètres sont personnels à l’utilisateur et doivent être gardés confidentiels. Ils permettent en particulier de contrôler l’activité des utilisateurs.<br>
    La loi n° 78-17 du 6 janvier 1978 relative à l’informatique, aux fichiers et aux libertés, définit les conditions dans lesquelles des traitements de données personnels peuvent être opérés. Elle institue au profit des personnes concernées par les traitements des droits que la présente invite à respecter, tant à l’égard des utilisateurs que des tiers.</h5>
<h3>4 Sanctions</h3>
<h5>Le manquement aux règles et mesures de sécurité de la présente charte est susceptible d’engager la responsabilité de l’utilisateur et d’entraîner à son encontre des avertissements, des limitations ou suspensions d’utiliser tout ou partie du SI, voire des sanctions disciplinaires, proportionnées la gravité des faits concernés.</h5>
<h3>5 Entrée en vigueur </h3>
